package claseradio;

public class radio
{
	private int tamaño;
	private String tipo;
	private String color;
	private String marca;
	private int modelo;
	
	public  radio()
	{}
	
		
	

		public int getTamaño()
		{
			return this.tamaño;
		}
		public void setTamaño(int t)
		{
			this.tamaño=t;			
		}
		public String getTipo()
		{
			return this.tipo;
		}
		public void setTipo(String i)
		{
			this.tipo=i;
		}
		public String getColor()
		{
			return this.color;
		}
		public void setColor(String c)
		{
			this.color=c;
		}
		public String getMarca()
		{
		return this.marca;	
		}
		public void setMarca(String m)
		{
			this.marca=m;
		}
		public int getModelo()
		{
			return this.modelo;
		}
		public void setModelo(int o)
		{
			this.modelo=o;
		}
	}



